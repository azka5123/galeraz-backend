const express = require('express');
const router = express.Router();
const { commentPost, uncommentPost } = require('../controllers/commentController');

router.post('/v1/comment/:id', commentPost);
router.delete('/v1/uncomment/:id', uncommentPost);

module.exports = router;

/**
 * @swagger
 * tags:
 *   name: Comment
 *   description: Operations related to commenting and uncommenting posts
 */

/**
 * @swagger
 * /api/comment/v1/comment/{id}:
 *   post:
 *     summary: Comment on a post by post id
 *     tags: [Comment]
 *     parameters:
 *       - in: path
 *         name: id
 *         schema:
 *           type: string
 *         required: true
 *         description: The ID of the post to comment on
 *     requestBody:
 *       required: true
 *       content:
 *         multipart/form-data:
 *           schema:
 *             type: object
 *             properties:
 *               content:
 *                 type: string
 *                 example: This is My First Comment
 *     responses:
 *       201:
 *         description: Post commented successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: success
 *                 message:
 *                   type: string
 *                   example: Post commented successfully
 *       400:
 *         description: Bad Request
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: error
 *                 message:
 *                   type: string
 *                   example: Invalid post ID
 *       500:
 *         description: Internal Server Error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: error
 *                 message:
 *                   type: string
 *                   example: Something went wrong
 */

